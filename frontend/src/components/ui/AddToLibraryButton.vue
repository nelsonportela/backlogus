<template>
  <div>
    <!-- Floating Action Button -->
    <button
      @click="openSearchModal"
      class="fixed bottom-6 right-6 z-50 w-14 h-14 bg-primary-600 hover:bg-primary-700 dark:bg-primary-500 dark:hover:bg-primary-600 text-white rounded-full shadow-lg hover:shadow-xl transition-all duration-200 transform hover:scale-105 flex items-center justify-center group"
      :title="`Add ${mediaTypeLabel}`">
      <svg
        class="w-6 h-6 group-hover:rotate-90 transition-transform duration-200"
        fill="none"
        stroke="currentColor"
        viewBox="0 0 24 24">
        <path
          stroke-linecap="round"
          stroke-linejoin="round"
          stroke-width="2"
          d="M12 6v6m0 0v6m0-6h6m-6 0H6" />
      </svg>
    </button>

    <!-- Search Modal -->
    <div
      v-if="isSearchModalOpen"
      class="fixed inset-0 z-50 overflow-y-auto"
      @click.self="closeSearchModal">
      <!-- Backdrop -->
      <div
        class="fixed inset-0 bg-black bg-opacity-50 transition-opacity"></div>

      <!-- Modal -->
      <div class="relative min-h-screen flex items-center justify-center p-4">
        <div
          class="relative bg-white dark:bg-gray-800 rounded-lg shadow-xl max-w-4xl w-full max-h-[90vh] overflow-hidden">
          <!-- Header -->
          <div
            class="flex items-center justify-between p-3 border-b dark:border-gray-700">
            <h2 class="text-base font-medium text-gray-900 dark:text-gray-100">
              Add {{ mediaTypeLabel }}
            </h2>

            <button
              @click="closeSearchModal"
              class="-top-2 -right-2 z-10 bg-red-500 text-white rounded-full min-w-[24px] min-h-[24px] flex items-center justify-center"
              title="Close">
              <svg
                class="w-3 h-3"
                fill="none"
                stroke="currentColor"
                viewBox="0 0 24 24"
                stroke-width="3">
                <path
                  stroke-linecap="round"
                  stroke-linejoin="round"
                  d="M6 18L18 6M6 6l12 12" />
              </svg>
            </button>
          </div>

          <!-- Search Content -->
          <div class="p-4">
            <!-- Search Input -->
            <div class="flex gap-2 mb-4">
              <div class="flex-1">
                <input
                  type="text"
                  v-model="searchQuery"
                  @input="debouncedSearch"
                  class="block w-full px-3 py-2 border border-gray-300 dark:border-gray-600 rounded-md shadow-sm bg-white dark:bg-gray-700 text-gray-900 dark:text-gray-100 focus:outline-none focus:ring-primary-500 focus:border-primary-500 dark:focus:ring-primary-400 dark:focus:border-primary-400"
                  :placeholder="`Search for ${mediaTypeLabel.toLowerCase()}...`" />
              </div>
            </div>

            <!-- Search Results -->
            <div class="max-h-96 overflow-y-auto">
              <!-- Loading -->
              <div
                v-if="loading"
                class="text-center py-6 text-gray-500 dark:text-gray-400">
                <div class="inline-flex items-center">
                  <svg
                    class="animate-spin -ml-1 mr-3 h-5 w-5 text-primary-500"
                    fill="none"
                    viewBox="0 0 24 24">
                    <circle
                      class="opacity-25"
                      cx="12"
                      cy="12"
                      r="10"
                      stroke="currentColor"
                      stroke-width="4"></circle>
                    <path
                      class="opacity-75"
                      fill="currentColor"
                      d="M4 12a8 8 0 018-8V0C5.373 0 0 5.373 0 12h4zm2 5.291A7.962 7.962 0 014 12H0c0 3.042 1.135 5.824 3 7.938l3-2.647z"></path>
                  </svg>
                  Searching...
                </div>
              </div>

              <!-- Results -->
              <div v-else-if="searchResults.length > 0" class="space-y-3 mr-6">
                <div
                  v-for="item in searchResults"
                  :key="item.id"
                  class="flex items-center justify-between p-3 border border-gray-200 dark:border-gray-600 rounded-lg hover:bg-gray-50 dark:hover:bg-gray-700 cursor-pointer"
                  @click="handleShowDetails(item)">
                  <div class="flex items-center space-x-4 min-w-0 flex-1">
                    <img
                      v-if="getImageUrl(item)"
                      :src="getImageUrl(item)"
                      :alt="getItemName(item)"
                      class="w-16 h-24 object-cover rounded flex-shrink-0" />
                    <div
                      v-else
                      class="w-16 h-24 bg-gray-200 dark:bg-gray-600 rounded flex items-center justify-center flex-shrink-0">
                      <span class="text-gray-400 dark:text-gray-500 text-xs"
                        >No Image</span
                      >
                    </div>
                    <div class="min-w-0 flex-1">
                      <h4
                        class="font-medium text-gray-900 dark:text-gray-100 text-sm truncate">
                        {{ getItemName(item) }}
                      </h4>
                      <p
                        v-if="getItemYear(item)"
                        class="text-sm text-gray-500 dark:text-gray-400">
                        {{ getItemYear(item) }}
                      </p>
                      <!-- TV Show specific info -->
                      <div
                        v-if="mediaType === 'show'"
                        class="text-sm text-gray-500 dark:text-gray-400">
                        <p v-if="item.seasons">
                          {{ item.seasons }}
                          {{ item.seasons === 1 ? "season" : "seasons" }}
                          <span v-if="item.episodes"
                            >({{ item.episodes }} episodes)</span
                          >
                        </p>
                        <p v-if="item.status" class="text-xs mt-1">
                          <span
                            :class="getStatusColor(item.status)"
                            class="px-2 py-0.5 rounded-full text-xs font-medium">
                            {{ item.status }}
                          </span>
                        </p>
                      </div>
                      <p
                        v-if="item.genres && item.genres.length > 0"
                        class="text-sm text-gray-500 dark:text-gray-400 truncate">
                        {{ item.genres.join(", ") }}
                      </p>
                      <p
                        v-if="item.platforms && item.platforms.length > 0"
                        class="text-sm text-gray-500 dark:text-gray-400 truncate cursor-pointer"
                        @click.stop="showAllPlatforms = item.id">
                        {{ item.platforms.slice(0, 2).join(", ") }}
                        <span
                          v-if="item.platforms.length > 2"
                          class="underline cursor-pointer">
                          +{{ item.platforms.length - 2 }} more
                        </span>
                      </p>
                      <!-- Full list modal -->
                      <div
                        v-if="showAllPlatforms === item.id"
                        class="fixed inset-0 z-50 flex items-center justify-center bg-black bg-opacity-50"
                        @click.self="showAllPlatforms = null">
                        <div
                          class="relative bg-white dark:bg-gray-800 rounded-lg shadow-xl border border-gray-200 dark:border-gray-700 max-w-sm w-full p-6">
                          <div class="flex items-center justify-between mb-3">
                            <h3
                              class="text-base font-semibold text-gray-900 dark:text-gray-100">
                              Platforms
                            </h3>
                            <button
                              @click="showAllPlatforms = null"
                              class="ml-2 bg-red-500 text-white rounded-full w-12 h-12 flex items-center justify-center"
                              title="Close">
                              <svg
                                class="w-4 h-4"
                                fill="none"
                                stroke="currentColor"
                                viewBox="0 0 24 24"
                                stroke-width="3">
                                <path
                                  stroke-linecap="round"
                                  stroke-linejoin="round"
                                  d="M6 18L18 6M6 6l12 12" />
                              </svg>
                            </button>
                          </div>
                          <ul class="space-y-1 max-h-48 overflow-y-auto">
                            <li
                              v-for="platform in item.platforms"
                              :key="platform"
                              class="text-gray-800 dark:text-gray-200 text-sm">
                              {{ platform }}
                            </li>
                          </ul>
                        </div>
                      </div>
                    </div>
                  </div>
                  <button
                    @click.stop="
                      !isItemInLibrary(getSearchResultId(item)) &&
                      handleAddToLibrary(item)
                    "
                    :disabled="isItemInLibrary(getSearchResultId(item))"
                    :class="[
                      'w-12 h-12 flex items-center justify-center rounded-full text-sm text-center focus:ring-4 focus:outline-none transition-all duration-200',
                      isItemInLibrary(getSearchResultId(item))
                        ? 'bg-green-600 text-white hover:bg-green-700 focus:ring-green-300 dark:bg-green-700 dark:hover:bg-green-800 dark:focus:ring-green-900'
                        : 'bg-blue-700 text-white hover:bg-blue-800 focus:ring-blue-300 dark:bg-blue-600 dark:hover:bg-blue-700 dark:focus:ring-blue-800',
                      'disabled:opacity-60 disabled:cursor-not-allowed',
                    ]"
                    :title="
                      isItemInLibrary(getSearchResultId(item))
                        ? 'Added'
                        : 'Add to Library'
                    "
                    type="button">
                    <svg
                      v-if="!isItemInLibrary(getSearchResultId(item))"
                      class="w-4 h-4"
                      aria-hidden="true"
                      xmlns="http://www.w3.org/2000/svg"
                      fill="none"
                      viewBox="0 0 20 20"
                      stroke="currentColor">
                      <path
                        stroke-linecap="round"
                        stroke-width="2.5"
                        d="M10 4v12M4 10h12" />
                    </svg>
                    <svg
                      v-else
                      class="w-4 h-4"
                      aria-hidden="true"
                      xmlns="http://www.w3.org/2000/svg"
                      fill="none"
                      viewBox="0 0 20 20"
                      stroke="currentColor">
                      <path
                        stroke-linecap="round"
                        stroke-linejoin="round"
                        stroke-width="2.5"
                        d="M6 10.5l3 3 5-6" />
                    </svg>
                    <span class="sr-only">
                      {{
                        isItemInLibrary(getSearchResultId(item))
                          ? "Added"
                          : "Add"
                      }}
                    </span>
                  </button>
                </div>
              </div>

              <!-- No results -->
              <div
                v-else-if="searchQuery && !loading"
                class="text-center py-6 text-gray-500 dark:text-gray-400">
                No {{ mediaTypeLabel.toLowerCase() }} found for "{{
                  searchQuery
                }}"
              </div>

              <!-- Initial state -->
              <div
                v-else
                class="text-center py-6 text-gray-500 dark:text-gray-400">
                Search for {{ mediaTypeLabel.toLowerCase() }} to add to your
                library
              </div>
            </div>
          </div>
        </div>
      </div>
    </div>

    <!-- Add to Library Modal -->
    <AddToLibraryModal
      :is-open="showAddToLibraryModal"
      :item="selectedItemForLibrary"
      :media-type="mediaType"
      @close="showAddToLibraryModal = false"
      @add-to-library="handleAddToLibraryFromModal" />
  </div>
</template>

<script setup>
import { ref, computed } from "vue";
const showAllPlatforms = ref(null);
import AddToLibraryModal from "../media/AddToLibraryModal.vue";

const isSearchModalOpen = ref(false);
const searchQuery = ref("");
const showAddToLibraryModal = ref(false);
const selectedItemForLibrary = ref(null);

// Debounce timer
let searchTimeout = null;

const props = defineProps({
  mediaType: {
    type: String,
    required: true,
    validator: (value) => ["game", "movie", "book", "show"].includes(value),
  },
  searchResults: {
    type: Array,
    default: () => [],
  },
  loading: {
    type: Boolean,
    default: false,
  },
  libraryItems: {
    type: Array,
    default: () => [],
  },
});

const emit = defineEmits([
  "search",
  "add-to-library",
  "show-details",
  "refresh-library",
]);

const mediaTypeLabel = computed(() => {
  const labels = {
    game: "Games",
    movie: "Movies",
    book: "Books",
    show: "Shows",
  };
  return labels[props.mediaType] || "Items";
});

const getSearchResultId = (item) => {
  if (props.mediaType === "game") return item.id;
  if (props.mediaType === "movie" || props.mediaType === "show")
    return item.tmdbId;
  if (props.mediaType === "book") return item.googleBooksId;
  return item.id;
};

const openSearchModal = () => {
  isSearchModalOpen.value = true;
};

const closeSearchModal = () => {
  isSearchModalOpen.value = false;
  searchQuery.value = "";
  // Also close any open add-to-library modal
  showAddToLibraryModal.value = false;
  selectedItemForLibrary.value = null;
  // Clear search results
  emit("search", "");
};

const debouncedSearch = () => {
  if (searchTimeout) clearTimeout(searchTimeout);
  searchTimeout = setTimeout(() => {
    if (searchQuery.value.trim()) {
      emit("search", searchQuery.value.trim());
    }
  }, 300);
};

const handleAddToLibrary = (item) => {
  selectedItemForLibrary.value = item;
  showAddToLibraryModal.value = true;
};

const handleAddToLibraryFromModal = (libraryData) => {
  emit("add-to-library", libraryData);
  showAddToLibraryModal.value = false;
  // Refresh the library to show the newly added item
  emit("refresh-library");
  // Keep search modal open in case user wants to add more items
};

const handleShowDetails = (item) => {
  emit("show-details", item);
  closeSearchModal();
};

const isItemInLibrary = (itemId) => {
  if (props.mediaType === "game") {
    // IGDB: search result id vs. library igdb_id
    return props.libraryItems.some((item) => item.igdb_id === itemId);
  } else if (props.mediaType === "movie") {
    // TMDB: search result id vs. library tmdb_id
    return props.libraryItems.some((item) => item.tmdbId === itemId);
  } else if (props.mediaType === "book") {
    // Books: search result id vs. library google_books_id (if used)
    return props.libraryItems.some((item) => item.google_books_id === itemId);
  } else if (props.mediaType === "show") {
    // Shows: search result id vs. library tmdb_id (if using TMDB for shows)
    return props.libraryItems.some((item) => item.tmdbId === itemId);
  } else {
    // Fallback: compare id fields directly
    return props.libraryItems.some((item) => item.id === itemId);
  }
};

const getImageUrl = (item) => {
  return item.cover_url || item.poster_url || item.image_url;
};

const getItemName = (item) => {
  return item.name || item.title;
};

const getItemYear = (item) => {
  const date =
    item.release_date ||
    item.publication_date ||
    item.air_date ||
    item.first_air_date;
  if (date) {
    return new Date(date).getFullYear();
  }
  return null;
};

const getStatusColor = (status) => {
  if (!status)
    return "bg-gray-100 text-gray-800 dark:bg-gray-700 dark:text-gray-300";

  const statusLower = status.toLowerCase();

  if (
    statusLower.includes("ended") ||
    statusLower.includes("canceled") ||
    statusLower.includes("cancelled")
  ) {
    return "bg-red-100 text-red-800 dark:bg-red-900/30 dark:text-red-400";
  } else if (
    statusLower.includes("returning") ||
    statusLower.includes("continuing") ||
    statusLower.includes("ongoing")
  ) {
    return "bg-green-100 text-green-800 dark:bg-green-900/30 dark:text-green-400";
  } else {
    return "bg-blue-100 text-blue-800 dark:bg-blue-900/30 dark:text-blue-400";
  }
};
</script>
